  <h1>FRAMEWORK</h1>
  <h2>Apa Itu Framework?</h2>
  <p>Framework adalah sebuah software yang berisi kerangka kerja berupa susunan fungsi, digunakan untuk membangun sebuah aplikasi berbasis web, mobile, maupun desktop.</p>
  <p>Konsep framework yaitu menyediakan susunan fungsionalitas generik. Suatu susunan fungsi yang umum ada pada suatu aplikasi. </p>
  <p>Programmer bisa menerapkan susunan fungsi tersebut pada aplikasi yang sedang dikembangkan. Programmer juga bisa merubah susunan fungsi secara selektif menggunakan kode tertentu.</p>
  <p>Framework sendiri mencakup pustaka kode, program dukungan, compiler, set tools, dan antarmuka pemrograman aplikasi atau API. </p>
  <p>Semua komponen tersebut tersusun menjadi satu kesatuan kerangka kerja, menjalankan peran dan fungsinya untuk membantu proyek pengembangan aplikasi.</p>
